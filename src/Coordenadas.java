public class Coordenadas {
   int x;
   int y;
   
   public Coordenadas(int x, int y){
      this.x = x;
      this.y = y;
   }
   public Coordenadas(){
   }

   public int getX() {
      return x;
   }

   public void setX(int x) {
      this.x = x;
   }

   public int getY() {
      return y;
   }

   public void setY(int y) {
      this.y = y;
   }

   
}
